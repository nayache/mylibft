#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dup;
	int		i;

	if ((dup = (char *)malloc(sizeof(char) * (ft_strlen(s1) + 1))) == NULL)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		dup[i] = s1[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
