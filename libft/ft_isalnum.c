int	ft_isalnum(int c)
{
	unsigned char d;

	d = (unsigned char)c;
	if ((d >= '0' && d <= '9') || (d >= 'a' && d <= 'z') ||
		(d >= 'A' && d <= 'Z'))
		return (1);
	else
		return (0);
}
